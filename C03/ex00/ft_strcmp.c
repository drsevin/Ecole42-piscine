int	ft_strcmp(char *s1, char *s2)
{
	int		syc;

	syc = 0;
	while (s1[syc] == s2[syc] && s1[syc] != '\0' && s2[syc] != '\0')
		syc++;
	return (s1[syc] - s2[syc]);
}
